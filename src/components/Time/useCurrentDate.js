import {useEffect, useState} from "react";

export const useCurrentDate = () => {
  const [time, setTime] = useState(new Date());

  useEffect(() => {
    const intervalId = setInterval(() => {
      setTime(new Date());
    }, 1000);

    return () => {
      clearInterval(intervalId);
    };
  }, [new Date()]);
  return time;
};
